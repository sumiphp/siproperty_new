                sidebar.classList.remove('sidebar-fixed');
                sidebar.style.top = ""; // Reset to default
            }
        } else {
           
            sidebar.classList.remove('sidebar-fixed');
            sidebar.style.top = ""; // Reset to default
        }
    }

   
    updateSidebarPosition();

    
